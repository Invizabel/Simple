from simple_engine import *

def main():
    lines = [(50, 50, 100, 50),
              (50, 50, 50, 100),
              (50, 100, 100, 100),
              (100, 100, 100, 150),
              (100, 150, 50, 150),
              (150, 50, 200, 50),
              (150, 50, 150, 150),
              (150, 150, 200, 150),
              (250, 50, 300, 50),
              (275, 50, 275, 150),
              (250, 150, 300, 150),
              (350, 50, 350, 150),
              (350, 50, 400, 150),
              (400, 150, 400, 50),
              (475, 50, 450, 150),
              (475, 50, 500, 150),
              (550, 50, 550, 150),
              (550, 50, 600, 75),
              (600, 75, 550, 100),
              (550, 100, 600, 150),
              (675, 150, 675, 100),
              (675, 100, 650, 50),
              (675, 100, 700, 50),]
    
    simple_init()
    simple_display(1280, 720, 2)
    
    running = True
    while running:
        simple_event()
        simple_fps(60)
        
        simple_background(128, 128, 128, 255)
        
        for line in lines:
            simple_line(0, 0, 255, 0, line[0], line[1], line[2], line[3])

        simple_update()
    
if __name__ == "__main__":
    main()
